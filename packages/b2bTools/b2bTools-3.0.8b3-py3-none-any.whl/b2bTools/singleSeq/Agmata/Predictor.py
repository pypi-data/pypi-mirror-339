from b2bTools.general.Io import B2bIo
from b2bTools.singleSeq.Agmata.agmata import getAgmataPredictions

class Agmata(B2bIo):

  version = '1.0'
  scriptName = "b2bTools.singleSeq.Agmata.Predictor"
  name = "Agmata"

  def __init__(self):
    self.allPredictions = {}
    # Additional info for writing files
    self.references = ['doi: 10.1093/bioinformatics/btz912  (2020)']
    self.infoTexts = ['Generated by Agmata', 'See http://bio2byte.be']

    self.informationPerPredictor = {
      self.name: {
        'references': self.references,
        'info':       ";".join(self.infoTexts),
        'version':    self.version,
        'origin':     self.scriptName
      }
    }

  # Warning: THIS IS NOW NOT WORKING FROM A DICT BUT FROM A LIST OF TUPLES!
  def predictSeqs(self, seqs):

    """
    :param seqs: List/tuple of (seqId,sequenceString) tuples
    :return: True if all sequences predicted, False if problems

    See self.allPredictions for the predictions themselves.
    """

    assert hasattr(self,'allPredictions') and self.allPredictions and self.allPredictions != {}, "Have to run DynaMine and EFoldMine prior to Agmata"

    agmataResults = getAgmataPredictions(seqs,efm=self)

    if not agmataResults:
      print("Agmata failed....")

    return (agmataResults)
