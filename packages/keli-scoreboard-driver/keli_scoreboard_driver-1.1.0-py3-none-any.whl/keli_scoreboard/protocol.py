def _calc_checksum(b_array: bytes) -> bytes:
    """
    Function fot getting text frame
    :param b_array: byte array for calculating checksum
    :return: byte array frame for text
    """
    return bytes([sum(b_array) % 256])


def _get_frame_length(ad: bytes, encoded_text: bytes) -> bytes:
    # Full text frame consist of whole package including
    # ad address + package length (2 bytes) + length of text byte and ending checksum (1 byte)
    total_length = len(ad) + 2 + len(encoded_text) + 1
    return total_length.to_bytes(2, 'big')


def get_text_frame(text: str, ad=_AD_ADDRESS_1) -> bytes:
    encoded_text = b''.join(_SYMBOL_TABLE[char] for char in text)
    raw_frame = ad + _get_frame_length(ad, encoded_text) + encoded_text
    return raw_frame + _calc_checksum(raw_frame)


def get_settings_bytes(
    scrolling_speed=_ScoreboardScrollingSpeed.SEC_2,
    stable_holding_time=_ScoreboardStableHoldingTime.INF,
    unit=_ScoreboardUnit.KG,
    ad_interval=64,
    display_date=False,
    display_time=False,
    display_week=False
) -> bytes:
    """
    Method for generating settings byte array for scoreboard
    1st byte - ??? (didn't change when was testing with a scoreboard program)
    2nd byte - total frame size (maybe): 16
    3rd byte - scrolling speed: 1 = 2 sec/line, 2 = 3 sec/line, ..., 5 = 6 sec/line
    4th byte - stable holding time: 0 = 10 sec, 1 = 30 sec, 2 = 60 sec, 3 = 2 min, 4 = 5 min, 5 = all time
    5th byte - unit: 0 = t, 1 = kg, 2 = No
    6th byte - interval between ad: 2*x (x <= 64)
    7th byte - display date: 0 = false, 1 = true
    8th byte - display time: 0 = false, 1 = true
    9th byte - display week: 0 = false, 1 = true
    other bytes - probably fillers (didn't change when was testing with a scoreboard program)
    :param scrolling_speed: selected scrolling speed
    :param stable_holding_time: selected stable holding time
    :param unit: selected unit
    :param ad_interval: ad interval <= 64
    :param display_date: flag for displaying date
    :param display_time: flag for displaying time
    :param display_week: flag for displaying week
    :return: byte array of settings
    """
    _SCOREBOARD_DISPLAY_SETTINGS_ADDRESS = b'\xff\xff\xff\x86'

    body = _SCOREBOARD_DISPLAY_SETTINGS_ADDRESS + bytes(
        [0, 16, scrolling_speed.value, stable_holding_time.value, unit.value, ad_interval * 2, int(display_date),
         int(display_time), int(display_week), 0, 0])

    return body + _calc_checksum(body)
